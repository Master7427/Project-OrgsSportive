<h3>Organisation Sportive?</h3>
<ul>
    <li>Organisation Sportive est une application web qui permet de gérer les informations relatives aux organisations sportives, y compris les équipes, les joueurs, les matchs et les statistiques.</li>
    <li>La page d'Accueil présente la liste sports avec une description et une equipe sportive
    <li>On y retrouve un lien pour se connecte </li>
    <ul>
        <li>Un utilisateur peut se connecter en utilisant son nom d'utilisateur et son mot de passe.</li>
        <li>Une fois connecté, l'utilisateur peut accéder à des fonctionnalités supplémentaires telles que la gestion des équipes, des joueurs et des matchs.</li>
</ul>
</p>
<h3>Technologies Utilisées</h3>
<ul>
    <li>PHP</li>
    <li>HTML/CSS</li>
</ul>
<h3>Fonctionnalités</h3>
<ul>
    <li>Authentification : Système de connexion pour les utilisateurs afin de sécuriser l'accès aux fonctionnalités avancées.</li>
    <li>Interface utilisateur conviviale : Conception responsive pour une utilisation sur différents appareils.</li>
</ul>
